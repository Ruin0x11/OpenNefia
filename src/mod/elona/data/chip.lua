local function make_chip_group(chips, group, source)
   for _, chip in ipairs(chips) do
      local width = 48
      local height = 48
      if chip.is_tall then
         height = 96
      end

      local x = (chip.elona_id % 33) * 48
      local y = math.floor(chip.elona_id / 33) * 48
      chip.count_x = chip.count_x or 1

      chip._id = group .. "_" .. chip._id
      chip._type = "base.chip"

      chip.image = {
         source = source,
         x = x,
         y = y,
         width = width,
         height = height,
         count_x = chip.count_x,
         count_y = 1
      }

      chip.group = group

      data:add(chip)
   end
end

